package br.com.dio;

public class Carro {
	
	String modelo;
	int ano;
	int qtdPessoas;
	
	
	public String getModelo() {
		return modelo;
				
	}
	
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	
	public int getAno() {
		return ano;
	}
	
	public void setAno(int ano) {
		this.ano = ano;
	}
	
	
	public int getQtdPessoas() {
		return qtdPessoas;
	}
	
	public void setQtdPessoas(int qtdPessoas) {
		this.qtdPessoas = qtdPessoas;
	}
	
	
	
}